import torch
import torch.nn as nn

print("==============================================================")
print("     FFN の Key-Value メモリ(辞書) 動作シミュレータ")
print("==============================================================\n")

# 特徴量次元のインデックス定義
# 入力ベクトル : [フランス度, 日本度, 観光度, 料理度] (4次元)
# 出力ベクトル : [フランス度, 日本度, 観光度, 高さ情報] (4次元)

class CustomFFN(nn.Module):
    def __init__(self):
        super().__init__()
        
        # 1. 第1層 (W_gate): キー検出器
        # 入力4次元 -> 中間層2次元 (2つの概念キー「フランス観光」「日本観光」)
        self.linear1 = nn.Linear(4, 2)
        
        # パラメータの手動設定 (AND ゲートの再現)
        # キー0: [1.0, 0.0, 1.0, 0.0] (フランス 且つ 観光)
        # キー1: [0.0, 1.0, 1.0, 0.0] (日本 且つ 観光)
        self.linear1.weight.data = torch.tensor([
            [1.0, 0.0, 1.0, 0.0],  # キー0
            [0.0, 1.0, 1.0, 0.0]   # キー1
        ])
        # バイアスを -1.0 に設定することで、
        # 「国」と「観光」の両方の特徴量が 1.0 で揃って初めて (1+1-1.0 = 1.0)、
        # 出力がプラスになる論理 AND ゲートを作ります。
        self.linear1.bias.data = torch.tensor([-1.0, -1.0])
        
        # 2. 活性化関数 GELU
        self.gelu = nn.GELU()
        
        # 3. 第2層 (W_down): 値回収器
        # 中間層2次元 -> 出力4次元
        self.linear2 = nn.Linear(2, 4)
        
        # キーに対応する事実値 (Value) の設定
        # 値0 (キー0に対応): [0.0, 0.0, 0.0, 330.0] (エッフェル塔の高さ330m)
        # 値1 (キー1に対応): [0.0, 0.0, 0.0, 634.0] (スカイツリーの高さ634m)
        # ※ 重み行列の形状は [出力次元, 入力次元] なので転置された形で登録します
        self.linear2.weight.data = torch.tensor([
            [0.0, 0.0],    # フランス度
            [0.0, 0.0],    # 日本度
            [0.0, 0.0],    # 観光度
            [330.0, 634.0] # 高さ情報 (キー0なら330m、キー1なら634mを引き出す)
        ])
        self.linear2.bias.data = torch.tensor([0.0, 0.0, 0.0, 0.0])

    def forward_step_by_step(self, x):
        # 順伝播の各ステップの数値を可視化して実行します
        print(f"入力ベクトル x : {x.tolist()}")
        
        # STEP 1: キー照合 (Linear 1)
        raw_keys = self.linear1(x)
        print(f"  [STEP 1] キー照合結果 (GELU前) : {raw_keys.tolist()}")
        print(f"            (フランス観光キー一致度: {raw_keys[0]:.1f} | 日本観光キー一致度: {raw_keys[1]:.1f})")
        
        # STEP 2: ゲート開閉 (GELU)
        gated_keys = self.gelu(raw_keys)
        print(f"  [STEP 2] ゲート通過結果 (GELU後) : {[round(v, 4) for v in gated_keys.tolist()]}")
        print(f"            (フランス観光ゲート: {'OPEN' if gated_keys[0] > 0.01 else 'CLOSED'} | 日本観光ゲート: {'OPEN' if gated_keys[1] > 0.01 else 'CLOSED'})")
        
        # STEP 3: 値の引き出し (Linear 2)
        retrieved_values = self.linear2(gated_keys)
        print(f"  [STEP 3] 引き出されたValueベクトル : {retrieved_values.tolist()}")
        
        # STEP 4: スキップ接続 (元の入力への足し込み)
        # 実際には Transformer は FFN の出力を元の入力 x に加算します
        final_output = x + retrieved_values
        print(f"  [STEP 4] 最終出力 (x + Value) : {final_output.tolist()}")
        print(f"            --> 復元された事実知識 (高さ情報): {final_output[3]:.1f} m\n")
        return final_output


# シミュレータの起動
model = CustomFFN()
model.eval()

# ------------------------------------------------------------
# ケース 1: フランスで観光中
# ------------------------------------------------------------
print("=== ケース 1: 入力『フランスで観光』 ===")
# [フランス=1, 日本=0, 観光=1, 料理=0]
x_france_tourism = torch.tensor([1.0, 0.0, 1.0, 0.0])
model.forward_step_by_step(x_france_tourism)

# ------------------------------------------------------------
# ケース 2: 日本で観光中
# ------------------------------------------------------------
print("=== ケース 2: 入力『日本で観光』 ===")
# [フランス=0, 日本=1, 観光=1, 料理=0]
x_japan_tourism = torch.tensor([0.0, 1.0, 1.0, 0.0])
model.forward_step_by_step(x_japan_tourism)

# ------------------------------------------------------------
# ケース 3: フランスで料理中 (AND条件の検証: 観光ではない)
# ------------------------------------------------------------
print("=== ケース 3: 入力『フランスで料理』(AND不発の検証) ===")
# [フランス=1, 日本=0, 観光=0, 料理=1]
# フランスだが「観光」ではないため、エッフェル塔の高さ知識は引き出されないはず
x_france_food = torch.tensor([1.0, 0.0, 0.0, 1.0])
model.forward_step_by_step(x_france_food)

print("==============================================================")
print("  シミュレーションのまとめ:")
print("  * ケース1では、フランスと観光が揃ったため、エッフェル塔の知識が発火しました。")
print("  * ケース2では、日本と観光が揃ったため、スカイツリーの知識が発火しました。")
print("  * ケース3では、フランスはあるものの観光ではないため、ゲートが閉じ、")
print("    高さ情報は引き出されませんでした。")
print("")
print("  [重要！] GELUの数理的な観察:")
print("  * 入力 1.0 に対し、出力の高さが 330m ではなく 277.6m (84.1%) に減衰しているのは、")
print("    GELU活性化関数が入力 1.0 を 0.8413 倍に滑らかに圧縮したためです。")
print("  * ケース3で高さが -99.6m とマイナスに少し漏れているのは、GELUが")
print("    負の入力 -1.0 を完全に 0.0 にせず、-0.1587 と極小のマイナス値を通すためです。")
print("    これが、ReLUと異なり『勾配消失を防ぎ、ニューロンの死を避ける』GELUの本質なのです。")
print("==============================================================")
