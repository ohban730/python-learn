import math
import torch

print("==============================================================")
print("  オッズ、オッズ比、ロジット、シグモイドの数理を学ぶデモ")
print("==============================================================\n")

# ------------------------------------------------------------
# 1. 確率から「オッズ (Odds)」の計算
# ------------------------------------------------------------
print("=== 1. 確率からオッズの計算 ===")

def prob_to_odds(p):
    """確率 p からオッズを計算する"""
    return p / (1.0 - p)

# 五分五分 (0.5)、高い確率 (0.8)、低い確率 (0.2) のオッズ
for p in [0.2, 0.5, 0.8]:
    odds = prob_to_odds(p)
    print(f"確率 p = {p:.1f} のとき : オッズ = {odds:.2f}")
print("=> 確率が 0.5 のときはオッズは 1.0 (等倍) になります。")
print("   確率が上がるにつれてオッズは無限大に向かって上昇します。\n")


# ------------------------------------------------------------
# 2. 「オッズ比 (Odds Ratio)」の計算（臨床試験シミュレーション）
# ------------------------------------------------------------
print("=== 2. 臨床試験におけるオッズ比の計算 ===")

# 新薬を飲んだグループAと、偽薬を飲んだグループBの治る確率
p_A = 0.8  # グループA (薬あり) の治る確率: 80%
p_B = 0.5  # グループB (薬なし) の治る確率: 50%

odds_A = prob_to_odds(p_A)
odds_B = prob_to_odds(p_B)

# オッズ比の計算
odds_ratio = odds_A / odds_B

print(f"グループA (服用群) のオッズ : {odds_A:.2f}")
print(f"グループB (非服用) のオッズ : {odds_B:.2f}")
print(f"オッズ比 (Odds Ratio)       : {odds_ratio:.2f}")
print("=> オッズ比が 4.0 ということは、薬を飲んだ人は治るオッズが")
print("   「4倍高い（＝薬の効果が非常に高い）」ことを意味します。\n")


# ------------------------------------------------------------
# 3. 「ロジット (Logit / 対数オッズ)」の計算
# ------------------------------------------------------------
print("=== 3. オッズの対数（ロジット）の計算 ===")

def logit(p):
    """ロジット関数: 確率 p を実数 x (ロジット) に変換する"""
    # 0 または 1 のときはゼロ除算や対数エラーになるため例外回避を入れます
    if p <= 0.0 or p >= 1.0:
        raise ValueError("確率 p は 0 より大きく 1 より小さい必要があります。")
    return math.log(prob_to_odds(p))

probabilities = [0.1, 0.3, 0.5, 0.7, 0.9]
logits = []

for p in probabilities:
    x = logit(p)
    logits.append(x)
    print(f"確率 p = {p:.1f} ──(ロジット化)──> ロジット x = {x:.4f}")
print("=> 確率 0.5 を中心(0)として、対称な実数値にマッピングされました。\n")


# ------------------------------------------------------------
# 4. 逆関数としての「シグモイド (Sigmoid)」の実証
# ------------------------------------------------------------
print("=== 4. ロジットの逆変換（シグモイド）による確率への復元 ===")

def sigmoid(x):
    """シグモイド関数: ロジット x を確率 p に逆変換する"""
    return 1.0 / (1.0 + math.exp(-x))

print("ロジット x をシグモイド関数に通して、元の確率に戻るか検証します:")
for x, original_p in zip(logits, probabilities):
    restored_p = sigmoid(x)
    print(f"ロジット x = {x: .4f} ──(シグモイド)──> 復元確率 p = {restored_p:.1f} (元: {original_p:.1f})")
print("=> 逆関数であるシグモイド関数によって、完全に元の確率へと復元されました！\n")


# ------------------------------------------------------------
# 5. PyTorch の torch.sigmoid との整合性確認
# ------------------------------------------------------------
print("=== 5. PyTorch の組み込みシグモイドとの比較 ===")

# 自作したシグモイドと、PyTorch の組み込み関数の計算結果が一致することを確認
test_x = 2.5
py_sigmoid_val = sigmoid(test_x)
torch_sigmoid_val = torch.sigmoid(torch.tensor(test_x, dtype=torch.float64)).item()

print(f"テスト値 x = {test_x} のとき:")
print(f"  自作 sigmoid(x)       : {py_sigmoid_val:.6f}")
print(f"  torch.sigmoid(x) の値 : {torch_sigmoid_val:.6f}")
print(f"  計算結果は一致するか？: {math.isclose(py_sigmoid_val, torch_sigmoid_val)}")
print("==============================================================")
