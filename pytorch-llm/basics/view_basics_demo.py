import torch

print("==============================================================")
print("  PyTorch view メソッドとメモリ上の挙動を学ぶデモプログラム")
print("==============================================================\n")

# ------------------------------------------------------------
# 1. 基本的な形状変更と「-1」による自動計算
# ------------------------------------------------------------
print("=== 1. 基本的な形状変更 ===")

# 要素数 6 個の 1次元テンソルを作成 (形状: [6])
x = torch.tensor([0, 1, 2, 3, 4, 5])
print(f"元のテンソル x:\n{x}")
print(f"元の形状: {x.shape}\n")

# [2行3列] に変形 (形状: [2, 3])
x_2x3 = x.view(2, 3)
print(f"x.view(2, 3) の結果:\n{x_2x3}")
print(f"形状: {x_2x3.shape}\n")

# [3行2列] に変形 (形状: [3, 2])
x_3x2 = x.view(3, 2)
print(f"x.view(3, 2) の結果:\n{x_3x2}")
print(f"形状: {x_3x2.shape}\n")

# 「-1」を指定して自動的にサイズを決定させる (形状: [3, 2])
# ※ 要素数が 6 個で、列数が 2 と指定されているため、行数は自動的に 6 / 2 = 3 と計算されます。
x_auto = x.view(-1, 2)
print(f"x.view(-1, 2) の結果 (自動決定):\n{x_auto}")
print(f"形状: {x_auto.shape}\n")


# ------------------------------------------------------------
# 2. メモリ上の実体 (Storage) の検証
# ------------------------------------------------------------
print("=== 2. メモリ上の実体の同一性検証 ===")

# PyTorchのテンソルは、Shapeがどうあれ、メモリの裏側では必ず「1次元の一列のデータ」として連続して保存されています。
# untyped_storage() を使うと、メモリ上の生の一列データを覗き見ることができます。
print(f"x のメモリ実体     : {x.untyped_storage()}")
print(f"x_2x3 のメモリ実体 : {x_2x3.untyped_storage()}")
print(f"x_3x2 のメモリ実体 : {x_3x2.untyped_storage()}")
print("=> 形状 (Shape) が異なっても、メモリ上のデータ配列の順序は全く同じであることが分かります。\n")

# 💡 メモリコピーが発生していない（データを共有している）ことの確認
print("元データ x の最初の要素を '99' に書き換えます...")
x[0] = 99

print(f"書き換え後の元データ x : {x}")
print(f"viewされた x_2x3 の中身 : \n{x_2x3}")
print("=> 元のテンソルを書き換えると、viewされた側のデータも連動して書き換わりました。")
print("   これは、viewメソッドが「新しいデータをコピー作成した」のではなく、")
print("   「同じメモリデータを違う Shape で読み取るルール（窓）」だけを適用した証拠です。\n")


# ------------------------------------------------------------
# 3. LLMのマルチヘッドアテンションでの多次元の分割
# ------------------------------------------------------------
print("=== 3. LLMで使われる多次元テンソルの分割 ===")

# バッチサイズ b=2, 単語数 num_tokens=3, 特徴量 d_out=8 の入力テンソルを想定
# 形状: [2, 3, 8] (3次元テンソル)
# ここでは分かりやすく 0 から始まる連続値で作成します。
batch_data = torch.arange(2 * 3 * 8).view(2, 3, 8)
print(f"元のバッチデータ (Shape: {batch_data.shape}):\n{batch_data}\n")

# ヘッド数 num_heads=2, 各ヘッドの次元 head_dim=4 に分割したい
# d_out (8次元) を、[num_heads (2) × head_dim (4)] に切り分けます。
# 形状を [2, 3, 8] から [2, 3, 2, 4] に view で変更します。
split_data = batch_data.view(2, 3, 2, 4)

print("--- [2, 3, 2, 4] に view 分割した後のデータ ---")
print(f"分割後の形状: {split_data.shape}")
print(f"バッチ0 の単語0 (元の特徴量: [0, 1, 2, 3, 4, 5, 6, 7]) の中身:")
print(f"  ヘッド0が担当する部屋 (前半 4次元): {split_data[0, 0, 0]}") # [0, 1, 2, 3]
print(f"  ヘッド1が担当する部屋 (後半 4次元): {split_data[0, 0, 1]}") # [4, 5, 6, 7]
print("\n=> メモリのコピーを一切行うことなく、テンソルの解釈を [8] から [2, 4] に")
print("   変更するだけで、一瞬で各ヘッド用の特徴部屋へと分割できたことが分かります。")
print("==============================================================")
