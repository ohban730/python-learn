import torch

print("==============================================================")
print("  PyTorch contiguous() の物理的な仕組みを学ぶデモプログラム")
print("==============================================================\n")

# ------------------------------------------------------------
# 1. テンソルの作成と連続性 (Contiguous) の確認
# ------------------------------------------------------------
print("=== 1. テンソルの作成と初期状態 ===")

# 2行2列のテンソルを作成
# メモリの実体（Storage）には一列に [1, 2, 3, 4] と並んでいます。
x = torch.tensor([[1, 2], 
                  [3, 4]])

print(f"元のテンソル x:\n{x}")
print(f"メモリ上の実体配列: {x.untyped_storage()}")
# is_contiguous() でメモリ上のデータが連続しているかを確認できます
print(f"メモリ上で連続しているか？ (x.is_contiguous()) : {x.is_contiguous()}")
print("=> 1行目の [1, 2] はメモリ上で隣り合っており、連続しています。\n")


# ------------------------------------------------------------
# 2. 転置 (transpose) による連続性の崩壊
# ------------------------------------------------------------
print("=== 2. 転置 (transpose) による連続性の崩壊 ===")

# 転置を適用して行と列を入れ替えます
x_t = x.t()

print(f"転置したテンソル x_t:\n{x_t}")
print(f"転置後のメモリ上の実体配列: {x_t.untyped_storage()}")
print(f"メモリ上で連続しているか？ (x_t.is_contiguous()) : {x_t.is_contiguous()}")
print("=> [重要] 転置された見た目では 1行目は [1, 3] です。")
print("   しかし、メモリ上の生データは [1, 2, 3, 4] のまま動いていないため、")
print("   1 と 3 はメモリ上で隣り合っておらず、飛び飛び（非連続）になっています。\n")


# ------------------------------------------------------------
# 3. 非連続な状態での view メソッドの実行（クラッシュの再現）
# ------------------------------------------------------------
print("=== 3. 非連続な状態での view の実行 ===")

# view メソッドは、「メモリ上の一続きのデータ」に対して新しい改行ルールを被せるため、
# メモリ配置が飛び飛び（Non-contiguous）になっているとエラーを吐いてクラッシュします。
try:
    print("x_t.view(4) を実行しようと試みます...")
    x_t.view(4)
except RuntimeError as e:
    print(f"\n[エラー 予測通りのエラーが発生しました！]")
    print(f"エラーメッセージ: {e}")
    print("=> メモリが連続していないため、PyTorchは新しい形状で読み替えることができません。\n")


# ------------------------------------------------------------
# 4. contiguous() によるメモリデータの整頓（おまじないの正体）
# ------------------------------------------------------------
print("=== 4. contiguous() によるメモリの整頓 ===")

# contiguous() を実行すると、飛び飛びになっていたデータを、
# 転置後の見た目の順序（[1, 3, 2, 4]）に合わせて、新しいメモリ領域にコピーして一列に整列し直します。
x_c = x_t.contiguous()

print(f"contiguous() を呼んだ後のテンソル x_c:\n{x_c}")
print(f"メモリ上の実体配列 (新しく並び変わっている): {x_c.untyped_storage()}")
print(f"メモリ上で連続しているか？ (x_c.is_contiguous()) : {x_c.is_contiguous()}")
print(f"x_t (転置後) のメモリ番地 : {x_t.data_ptr()}")
print(f"x_c (整頓後) のメモリ番地 : {x_c.data_ptr()}")
print("=> メモリ番地（アドレス）が変化し、データが綺麗に一列にコピーし直されたことが分かります。\n")

# 再びメモリが連続（Contiguous）になったため、viewが正常に実行できるようになります。
print("x_c.view(4) を実行します...")
x_flat = x_c.view(4)
print(f"view(4) の実行成功！: {x_flat}")
print("==============================================================")
